package Practice51_100;

public class Task052DoWhileLoop {

	public static void main(String[] args) {
		/*
		 * For you to do: 
Create a do while loop that prints out "Hello World!" 5 times. 

Output:
Hello World!
Hello World!
Hello World!
Hello World!
Hello World! 
		 */

		int i = 1;
		do {
			System.out.println("Helow wold");
			i++;
		}while (i<=5);
		
	}

}
