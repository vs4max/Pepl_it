package Practice51_100;

public abstract class Task068NestedForLoop {

	public static void main(String[] args) {
		/*
		 * Write a program to print out the pattern: 

 
$$$$
$  $
$  $
$$$$
		 */
		
		for (int a=1; a<=4; a++) {
			System.out.print("$");
		}System.out.println();
		for(int b=1; b<=2; b++) {
			System.out.print("$"+"  ");
		}System.out.println();
		for(int b=1; b<=2; b++) {
			System.out.print("$"+"  ");
		}System.out.println();
		for (int a=1; a<=4; a++) {
			System.out.print("$");
	}
	}
}
